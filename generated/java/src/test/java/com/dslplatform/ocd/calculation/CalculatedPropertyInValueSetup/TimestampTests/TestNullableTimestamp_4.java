package com.dslplatform.ocd.calculation.CalculatedPropertyInValueSetup.TimestampTests;

import com.dslplatform.ocd.calculation.CalculatedPropertyInValueSetup.NullableTimestamp_4;

public class TestNullableTimestamp_4 {

    /* Testing the property field private visibility via reflection (no instantiation) */
    @org.junit.Test
    public void testPropertyFieldVisibility() throws NoSuchFieldException {
        org.junit.Assert.assertEquals(
                java.lang.reflect.Modifier.PRIVATE,
                NullableTimestamp_4.class.getDeclaredField(
                        "nullableTimestamp").getModifiers());
    }

    /* Testing the property field class via reflection (no instantiation) */
    @org.junit.Test
    public void testPropertyFieldClass() throws NoSuchFieldException {
        org.junit.Assert.assertEquals(
                org.joda.time.DateTime.class,
                NullableTimestamp_4.class.getDeclaredField(
                        "nullableTimestamp").getType());
    }
}

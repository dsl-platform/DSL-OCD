package com.dslplatform.ocd.calculation.CalculatedPropertyInAggregateSetup.IntegerTests;

import com.dslplatform.ocd.calculation.CalculatedPropertyInAggregateSetup.NullableInteger_3;

public class TestNullableInteger_3 {

    /* Testing the property field private visibility via reflection (no instantiation) */
    @org.junit.Test
    public void testPropertyFieldVisibility() throws NoSuchFieldException {
        org.junit.Assert.assertEquals(
                java.lang.reflect.Modifier.PRIVATE,
                NullableInteger_3.class.getDeclaredField(
                        "nullableInteger").getModifiers());
    }

    /* Testing the property field class via reflection (no instantiation) */
    @org.junit.Test
    public void testPropertyFieldClass() throws NoSuchFieldException {
        org.junit.Assert.assertEquals(
                Integer.class,
                NullableInteger_3.class.getDeclaredField(
                        "nullableInteger").getType());
    }
}
